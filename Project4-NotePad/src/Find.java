    private void findBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findBtnActionPerformed
        
        String textFieldValue = findTextField.getText();
        textArea = parentFrame.getTextArea();
        
        if(searchValue == null)
        {
            lastIndexViewed = 0;
            searchValue = textFieldValue;
            populateFindList();
            if(position.isEmpty())
                return;
            parentFrame.selectText(position, searchValue, lastIndexViewed, downRadio.isSelected());
            
           
        }
        else if(!searchValue.equals(textFieldValue))
        {
            //new find requested, so populate new list and highlight first index
            lastIndexViewed = 0;
            searchValue = textFieldValue;
            position.clear();
            populateFindList();
            parentFrame.selectText(position, searchValue, lastIndexViewed, downRadio.isSelected());
            
        }
        else
        {
            //existing find, check whether to find forward or back
            if (downRadio.isSelected())
            {
                if(lastIndexViewed + 1 > position.size() - 1)
                {
                    JOptionPane.showMessageDialog(this, "Cannot find: '" + searchValue + "'");
                    return;
                }
                lastIndexViewed++;
                parentFrame.selectText(position, searchValue, lastIndexViewed, true);
            }
            else
            {
                if(lastIndexViewed -1 < 0)
                {
                    JOptionPane.showMessageDialog(this, "Cannot find: '" + searchValue + "'");
                    return;
                }
                lastIndexViewed--;
                parentFrame.selectText(position, searchValue, lastIndexViewed, false);
            }
        }
    }//GEN-LAST:event_findBtnActionPerformed

