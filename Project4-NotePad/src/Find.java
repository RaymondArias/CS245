    public void populateFindList()
    {
        if(searchValue.length() == 0)
            return;
        while(true)
        {
            int indexOfNextFind;
            
            if(position.isEmpty())
            {
                indexOfNextFind = textArea.getText().indexOf(searchValue);
                if (indexOfNextFind == -1)
                    break;
                position.add(indexOfNextFind);
            }
            else
            {
                 indexOfNextFind = textArea.getText().indexOf(searchValue, position.getLast() + 1);
                if (indexOfNextFind == -1)
                    break;
                position.add(indexOfNextFind);
            }
            
        }
       
            
        
    }

